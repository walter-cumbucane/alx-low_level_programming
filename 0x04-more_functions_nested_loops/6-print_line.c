#include "main.h"

/**
 *print_line -checks for a lowercase character
 *@n: letter being tested
 * Return: Always 0
 */

void print_line(int n)
{
	if (n <= 0)
	{
		_putchar('\n');
	}
	else
	{
		int i;

		i = 1;
		while (i <= n)
		{
			_putchar('_');
			if (i == n)
			{
				_putchar('\n');
			}
			i++;
		}
	}
}
